//Update Cart Total
function updateTotal() {
  var cartItemContainer = document.getElementsByClassName('cart-items')[0]  //1st item

  var cartRows = cartItemContainer.getElementsByClassName('cart-row')

var total = 0

  for (var i =0; i < cartRows.length; i++) {
    var cartRow = cartRow[i];
    var priceElem = cartRow.getElementsByClassName('card-text')[0]
    var quantityElem = cartRow.getElementsByClassName('total-count')[0]
    var price = parseFloat(priceElem.innerText.replace('$', ''))
    var quantity = quantityElem.value
    total = total + (price * quantity)



  }
  document.getElementsByClassName('total-count')[0].innerText = '&' + total
}